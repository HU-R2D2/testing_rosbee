#ifndef _MUTEX_H
#define _MUTEX_H


/// @brief Class for synchronisation between cogs.
///
/// The Mutex class provides simple synchronisation between cogs
/// using a binary semaphore.
/// The mutex can either be locked or unlocked and does not hold a owner.
/// This means that it cannot be used recursively.
class Mutex{
private:
  volatile bool semaphore;

public:
 
  /// @brief Create new Mutex object.
  Mutex();
  
  /// @brief Claim the mutex.
  ///
  /// This function blocks if the mutex is already claimed.
  void claim() volatile;
  
  /// @brief Free the mutex.
  void signal() volatile; 
  
};  

#endif // _MUTEX_H